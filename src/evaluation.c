short OpenRookFile(int square, Game* game) {
	int file = square % 8;
	short open = 30;
	for (int i = 0; i < 7; i++)
	{
		file += 8;
		if ((game->Squares[file] & PAWN)) //smi open
			open -= 15;
	}
	return open;
}

short DoublePawns(int square, Game* game, PieceType pawn) {
	int file = square % 8;
	short score = -9; //Always one pawn
	for (int i = 0; i < 7; i++)
	{
		file += 8;
		if ((game->Squares[file] == pawn)) //semi open
			score += 9;
	}
	return score;
}

short GetEval(Game* game) {
	short score = 0;
	short neg[] = { -1, 1 };
	for (size_t s = 0; s < 2; s++)
	{
		for (int p = 0; p < 16; p++)
		{
			Piece piece = game->Pieces[s][p];
			int i = piece.SquareIndex;
			if (piece.Off)
				continue;
			PieceType pt = game->Pieces[s][p].Type & 7;
			switch (pt)
			{
			case ROOK:
			{
				score += ( neg[s] * OpenRookFile(i, game));

				// connected rooks, no king between
			}
			//case BISHOP:
			//{
			//	//todo: bad bishops

			//	//outposts, protected by a pawn?
			//}
			//case KNIGHT: {
			//	//outposts, protected by a pawn?
			//}
			case PAWN: {
				score -= (neg[s] * DoublePawns(i, game, piece.Type));
			}
			default:
				break;
			}
		}
	}
	return score;
}